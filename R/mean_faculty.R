#  This file contains the mean function that calculates the average age of
#  all current Williams Faculty.


mean_faculty <- function(){

  yearBA <- as.numeric(as.character(facultyData$V3))

  #Vector of the years at which faculty obtained their BA's

  2038

  # 2038 = 2016 + 22. We add the current year, 2016, plus the assumed age at
  # which a BA's is obtained, 22, and then subtract by the average year in which
  # Williams Faculty obtained their BA's. Thus, we obtain the estimate average
  # age of Williams faculty.

  - mean(yearBA,na.rm=TRUE)

  # Takes the mean of the years in which faculty obtained their BA's

}
